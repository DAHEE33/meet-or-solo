CREATE INDEX idx_tour_places_type_status ON tour_places (content_type_id, status);
CREATE INDEX idx_tour_places_content_id ON tour_places (content_id);

CREATE TABLE tour_api_call_logs (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY,
    operation_name VARCHAR(100) NOT NULL,
    request_key VARCHAR(255),
    mobile_app VARCHAR(50) NOT NULL,
    status_code INTEGER,
    success BOOLEAN NOT NULL,
    response_time_ms INTEGER,
    result_count INTEGER,
    error_message VARCHAR(1000),
    called_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    created_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT pk_tour_api_call_logs PRIMARY KEY (id),
    CONSTRAINT chk_tour_api_call_logs_mobile_app CHECK (mobile_app = 'SoloIn'),
    CONSTRAINT chk_tour_api_call_logs_response_time CHECK (response_time_ms IS NULL OR response_time_ms >= 0),
    CONSTRAINT chk_tour_api_call_logs_result_count CHECK (result_count IS NULL OR result_count >= 0)
);

CREATE INDEX idx_tour_api_call_logs_operation_called_at ON tour_api_call_logs (operation_name, called_at);
CREATE INDEX idx_tour_api_call_logs_success_called_at ON tour_api_call_logs (success, called_at);

CREATE TABLE festival_checkins (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY,
    member_id BIGINT NOT NULL,
    festival_id BIGINT NOT NULL,
    distance_meters INTEGER NOT NULL,
    status VARCHAR(30) NOT NULL DEFAULT 'ACTIVE',
    checked_in_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    expires_at TIMESTAMPTZ NOT NULL,
    created_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT pk_festival_checkins PRIMARY KEY (id),
    CONSTRAINT fk_festival_checkins_member FOREIGN KEY (member_id) REFERENCES members (id) ON DELETE RESTRICT,
    CONSTRAINT fk_festival_checkins_festival FOREIGN KEY (festival_id) REFERENCES festivals (id) ON DELETE RESTRICT,
    CONSTRAINT chk_festival_checkins_status CHECK (status IN ('ACTIVE', 'EXPIRED', 'CANCELLED')),
    CONSTRAINT chk_festival_checkins_distance CHECK (distance_meters >= 0),
    CONSTRAINT chk_festival_checkins_expires_at CHECK (expires_at > checked_in_at)
);

CREATE UNIQUE INDEX uq_festival_checkins_member_festival_active
    ON festival_checkins (member_id, festival_id)
    WHERE status = 'ACTIVE';

CREATE INDEX idx_festival_checkins_member_active ON festival_checkins (member_id, status, expires_at);
CREATE INDEX idx_festival_checkins_festival_active ON festival_checkins (festival_id, status, expires_at);
CREATE INDEX idx_festival_checkins_expires_at ON festival_checkins (expires_at);
